CREATE TABLE raw_source_points AS
SELECT accuracy_meters,
       timestamp,
       user_id,
       geom,
       elevation_meters,
       NULL::bigint AS device_id,
       ignored,
       invalid
FROM raw_location_points
WHERE synthetic = FALSE;

