CREATE EXTENSION IF NOT EXISTS btree_gist;

-- add time line override table
CREATE TABLE timeline_overrides
(
    id         bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id    bigint                      NOT NULL REFERENCES users (id),
    device_id  bigint                      NOT NULL REFERENCES devices (id),
    start_time timestamp(6) with time zone NOT NULL,
    end_time   timestamp(6) with time zone NOT NULL,
    active     boolean                  DEFAULT TRUE,
    created_at timestamp with time zone DEFAULT NOW(),
    CONSTRAINT no_timeline_overlap EXCLUDE USING gist (
        user_id WITH =,
        TSTZRANGE(start_time, end_time) WITH &&
        )
);

-- Index for the View to lookup overrides quickly
CREATE INDEX idx_timeline_overrides_active
    ON timeline_overrides (user_id, start_time, end_time)
    WHERE active = TRUE;


CREATE OR REPLACE VIEW v_source_stream AS
WITH current_overrides AS (
    -- Get active overrides for the time of the query
    SELECT user_id, device_id, start_time, end_time
    FROM timeline_overrides
    WHERE active = true
)
-- PART A: Points from specific overridden devices
SELECT
    rsp.id AS source_point_id, -- <--- Crucial back-link
    rsp.accuracy_meters,
    rsp.timestamp,
    rsp.user_id,
    rsp.geom,
    rsp.elevation_meters,
    rsp.device_id
FROM raw_source_points rsp
         JOIN current_overrides ov ON rsp.user_id = ov.user_id
    AND rsp.device_id = ov.device_id
    AND rsp.timestamp >= ov.start_time
    AND rsp.timestamp <= ov.end_time

UNION ALL

-- PART B: Points from the "Main" device (where no override exists)
SELECT
    rsp.id AS source_point_id, -- <--- Crucial back-link
    rsp.accuracy_meters,
    rsp.timestamp,
    rsp.user_id,
    rsp.geom,
    rsp.elevation_meters,
    rsp.device_id
FROM raw_source_points rsp
WHERE rsp.device_id IS NULL  -- This is our "Main" device convention
  AND NOT EXISTS (
    -- Only take the main point if there is NO override for this specific second
    SELECT 1
    FROM current_overrides ov
    WHERE ov.user_id = rsp.user_id
      AND rsp.timestamp >= ov.start_time
      AND rsp.timestamp <= ov.end_time
);
