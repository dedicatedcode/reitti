    ADD COLUMN id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY;

CREATE UNIQUE INDEX idx_raw_source_upsert_target
    ON raw_source_points (user_id, timestamp, device_id)
    NULLS NOT DISTINCT;

-- 3. Add the Partial Indices (The secret to performance)
CREATE INDEX idx_raw_source_main_device
    ON raw_source_points (user_id, timestamp)
    WHERE device_id IS NULL;

CREATE INDEX idx_raw_source_external_devices
    ON raw_source_points (device_id, timestamp)
    WHERE device_id IS NOT NULL;


CREATE TABLE raw_location_points_new AS
SELECT rlp.id,
       rlp.accuracy_meters,
       rlp.timestamp,
       rlp.user_id,
       rlp.geom,
       rlp.processed,
       rlp.version,
       rlp.elevation_meters,
       rlp.synthetic,
       rlp.ignored,
       rlp.invalid,
       CASE
           WHEN rlp.synthetic = FALSE THEN rsp.id
           ELSE NULL
           END AS source_point_id
FROM raw_location_points rlp
         LEFT JOIN raw_source_points rsp
                   ON rlp.user_id = rsp.user_id
                       AND rlp.timestamp = rsp.timestamp;
